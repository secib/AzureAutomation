function Get-ModifiedFolderPathFromGitPushEvent
{
    [CmdletBinding()]
    param (
        [Parameter()]
        [string]
        $Body
    )

    $object = $Body | ConvertFrom-Json
    return , ($object.commits.modified | ForEach-Object { $_.Substring(0, $_.lastIndexOf('/')) } | Sort-Object -Unique)
}

